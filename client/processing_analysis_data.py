# format scientific labels:
def format_labels(label):
  # round the number, then add K (3429 -> 3K);
  # if it's less than 1,000, just return the number
  # if it's more than 1,000,000, round to nearest million and add M
  if label < 1000:
    return label
  elif label >= 1000 and label < 1000000:
    return str(round(label, -3))[:-3]+'K'
  else:
    return str(round(label, -6))[:-6]+'M'

