def ccpa_subjection_privacy_string_breakdown(df, url_string):
  # url_string is defined globally
  # swn = sites_with_nothing(df) # this is for not including well-known
  # this is for including well-known
  # # print(df.columns)
  # swn = df[((pd.isnull(df['usps_before_gpc']) == True) & (pd.isnull(df['usps_after_gpc']) == True)) # USPS
  #           & ((pd.isnull(df['OptanonConsent_before_gpc']) == True) & (pd.isnull(df['OptanonConsent_after_gpc']) == True)) # OAC
  #           & ((pd.isnull(df['gpp_before_gpc']) == True) & (pd.isnull(df['gpp_after_gpc']) == True)) #GPP
  #           & (~df['Well-known'].str.contains("'gpc': True")) ] # this last line must be changed to use wellknown collected by crawler (cast as string first)

  swn = df[((pd.isnull(df['usps_before_gpc']) == True) & (pd.isnull(df['usps_after_gpc']) == True)) # USPS
            & ((pd.isnull(df['OptanonConsent_before_gpc']) == True) & (pd.isnull(df['OptanonConsent_after_gpc']) == True)) # OAC
            & ((pd.isnull(df['gpp_before_gpc']) == True) & (pd.isnull(df['gpp_after_gpc']) == True)) #GPP
            & (~df['Well-known'].str.contains("'gpc'", na=False)) ] # this last line must be changed to use wellknown collected by crawler (cast as string first)
  no_implementation_no_third_party = swn[swn['third_party_count'] == 0] # these are sites that have no integrated SAFG network
  no_implementation_third_party = swn[swn['third_party_count'] != 0] ##########

  no_implementation_know_subject = no_implementation_third_party[no_implementation_third_party['third_party_urls'].str.contains(url_string, na=False)] #these are sites that have an SAFG network we did manually analyze
  no_implementation_may_be_subject = no_implementation_third_party[~no_implementation_third_party['third_party_urls'].str.contains(url_string, na=False)] #these are sites that have an SAFG network we didn't manually analyze.


  # now we want to split up the sites with some implementation into sites we know are subject and sites that may be subject
  sws = pd.concat([df,swn]).drop_duplicates(subset=['Site URL'],keep=False)
  some_implementation_no_third_party = sws[sws['third_party_count'] == 0]

  some_implementation_third_party = sws[sws['third_party_count'] != 0] ###########
  some_implementation_know_subject = some_implementation_third_party[some_implementation_third_party['third_party_urls'].str.contains(url_string, na=False)]
  some_implementation_may_be_subject = some_implementation_third_party[~some_implementation_third_party['third_party_urls'].str.contains(url_string, na=False)]
  return no_implementation_no_third_party, no_implementation_know_subject, no_implementation_may_be_subject, some_implementation_no_third_party, some_implementation_know_subject, some_implementation_may_be_subject

