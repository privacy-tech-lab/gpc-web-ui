"""The pie chart is difficult to view if all the smallest values are next to each other (because the labels overlap). To combat this, we will order the data [max, min, 2nd highest, 2nd lowest,....]."""

def order_data_for_pie_chart(data):
  data_sorted = {k: v for k, v in sorted(data.items(), key=lambda item: item[1])}
  data_keys = list(data_sorted.keys())
  ordered_keys = []
  ordered_values = []
  while len(data_keys) > 0:
    if len(data_keys) % 2 == 0:
      last = data_keys.pop(len(data_keys)-1)
      ordered_keys.append(last)
      ordered_values.append(data_sorted[last])
    else:
      first = data_keys.pop(0)
      ordered_keys.append(first)
      ordered_values.append(data_sorted[first])

  return ordered_keys, ordered_values

