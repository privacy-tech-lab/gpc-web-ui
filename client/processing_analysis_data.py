def sections_histogram_multisection(df, col)  :
  sections_hist = {'none':0}
  def extract_sections(row, column):
    decoded_gpp = row[column]
    if decoded_gpp is None or decoded_gpp == {}:
      if 'none' in sections_hist:
        sections_hist['none'] += 1
      else:
        sections_hist['none'] = 1
    else:
      s = str(list(decoded_gpp.keys()))
      if s in sections_hist:
        sections_hist[s] += 1
      else:
        sections_hist[s] = 1


  df.apply(extract_sections, column=col, axis=1)
  return (sections_hist)

def sections_histogram_single_section(df, col)  :
  sections_hist = {'none':0}
  def extract_sections(row, column):
    decoded_gpp = row[column]
    if decoded_gpp is None or decoded_gpp == {}:
      if 'none' in sections_hist:
        sections_hist['none'] += 1
      else:
        sections_hist['none'] = 1
    else:
      s = list(decoded_gpp.keys())

      for i in s:
        if i in sections_hist:
          sections_hist[i] += 1
        else:
          sections_hist[i] = 1


  df.apply(extract_sections, column=col, axis=1)

  return (sections_hist)

