def create_opt_outs_table(df):
    """
    Build a one‑row‑per‑mechanism summary of sites that **fully** opt out via
    each privacy mechanism (USP String, GPP String, OptanonConsent cookie).

    Parameters
    ----------
    df : pandas.DataFrame
        Filtered crawl dataframe.  Must contain the columns produced by
        `compute_USPS_OAC_GPP_distribution`, including a unique ``site_id``.

    Returns
    -------
    pandas.DataFrame
        Columns:
        * ``Mechanism`` – The name of the privacy mechanism.
        * ``Count``     – Number of distinct sites that fully opt out via it.
    """
    # Split into the 7 mutually‑exclusive buckets expected by the existing
    # helper utilities.
    a, b, c, d, e, f, g = compute_USPS_OAC_GPP_distribution(df)

    # Grab the FULL‑opt‑out subsets for each bucket.
    a1, b1, c1, d1, e1, f1, g1 = compute_full_opt_out(a, b, c, d, e, f, g)

    # Utility to count unique site identifiers across several dataframes.
    def _count_unique(dfs):
        ids = set()
        for _df in dfs:
            if 'site_id' in _df.columns:
                ids.update(_df['site_id'].tolist())
            else:
                # Fallback: use URL if ``site_id`` is absent.
                ids.update(_df['Site URL'].tolist())
        return len(ids)

    counts = {
        'USP String':      _count_unique([a1, c1, e1, g1]),
        'GPP String':      _count_unique([b1, c1, f1, g1]),
        'OptanonConsent':  _count_unique([d1, e1, f1, g1]),
    }

    return pd.DataFrame({'Mechanism': list(counts.keys()),
                         'Count': list(counts.values())})


