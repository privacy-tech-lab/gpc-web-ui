def intersection(fun1, fun2, df):
  return pd.merge(fun1(df),fun2(df), how='inner', on=['Site URL', 'site_id', 'status', 'domain', 'sent_gpc', 'urlClassification' ])

def no_opt_out_usps(df):
  # the site just needs to be not opted out after gpc. This means the string could be None, or the 3rd char could be - or N
   return df[~((pd.isnull(df['usps_after_gpc']) == False) & (df['usps_after_gpc'].str.contains(r'^..[yY].$'))) ]
def no_opt_out_OAC(df):
  # the site just needs to be opted out after gpc
   return df[~((pd.isnull(df['OptanonConsent_after_gpc']) == False) & (df['OptanonConsent_after_gpc'] == 'isGpcEnabled=1'))]
def no_opt_out_GPP(df_gpp):
  df = sections_table(df_gpp)
  # drop the sites that opt out via gpp from the full table of gpp sites
  return pd.concat([df, opt_out_gpp(df_gpp)]).drop_duplicates(keep=False, subset=['Site URL'])

def usps_gpp_partial(df):
  usps_no_GPP = intersection(opt_out_usps, no_opt_out_GPP, df)
  no_usps_GPP = intersection(no_opt_out_usps, opt_out_gpp, df)
  return usps_no_GPP, no_usps_GPP
def usps_OAC_partial(df):
  usps_no_OAC = intersection(opt_out_usps, no_opt_out_OAC, df)
  no_usps_OAC = intersection(no_opt_out_usps, opt_out_OAC, df)
  return usps_no_OAC, no_usps_OAC
def gpp_OAC_partial(df):
  gpp_no_OAC = intersection(opt_out_gpp, no_opt_out_OAC, df)
  no_gpp_OAC = intersection(no_opt_out_GPP, opt_out_OAC, df)
  return gpp_no_OAC, no_gpp_OAC
def no_opt_out(a,b,c,d,e,f,g):
  # no opt out means site does not opt out via any implemented privacy strings
  a_no_opt_out = no_opt_out_usps(a)
  b_no_opt_out = no_opt_out_GPP(b)
  c_no_opt_out = intersection(no_opt_out_usps,no_opt_out_GPP,c)
  d_no_opt_out = no_opt_out_OAC(d)
  e_no_opt_out = intersection(no_opt_out_usps,no_opt_out_OAC,e)
  f_no_opt_out = intersection(no_opt_out_GPP,no_opt_out_OAC,f)
  g_no_opt_out = no_opt_out_OAC(intersection(no_opt_out_usps,no_opt_out_GPP,g))
  return (a_no_opt_out, b_no_opt_out, c_no_opt_out, d_no_opt_out, e_no_opt_out, f_no_opt_out, g_no_opt_out)
def compute_full_opt_out(a,b,c,d,e,f,g):
  # full opt out means the site opts out via all implemented privacy strings
  a1 = opt_out_usps(a)
  b1 = opt_out_gpp(b)
  c1 = intersection(opt_out_usps,opt_out_gpp,c)
  d1 = opt_out_OAC(d)
  e1 = intersection(opt_out_usps,opt_out_OAC,e)
  f1 = intersection(opt_out_gpp,opt_out_OAC,f)
  g1 = opt_out_OAC(intersection(opt_out_usps,opt_out_gpp,g))
  return (a1, b1, c1, d1, e1, f1, g1)

